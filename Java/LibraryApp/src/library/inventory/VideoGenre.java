package library.inventory;

/** 
 * Enum is a special "class" that represents a group of constants 
 * Only six valid Genres, which are DOCUMENTARY, FAMILY, ACTION, ROMANCE, ANIMATED, and CHILDREN.
 * 
 * @author Thomas Marxsen
 * @version 1.0
 * @since 2022.03.23
 */
public enum VideoGenre {
	/**
	 * Genre for documentary video items.
	 */
	DOCUMENTARY,

	/**
	 * Genre for family video items.
	 */
	FAMILY,

	/**
	 * Genre for action video items.
	 */
	ACTION,

	/**
	 * Genre for romance video items.
	 */
	ROMANCE,

	/**
	 * Genre for animated video items.
	 */
	ANIMATED,

	/**
	 * Genre for children video items.
	 */
	CHILDREN
}
