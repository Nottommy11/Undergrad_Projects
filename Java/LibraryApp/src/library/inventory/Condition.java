package library.inventory;

/** 
 * Enum is a special "class" that represents a group of constants 
 * Only four valid Conditions, which are NEW, GOOD, FAIR, and POOR.
 * 
 * @author Thomas Marxsen
 * @version 1.0
 * @since 2022.03.29
 */
public enum Condition {
	/**
	 * Condition for new inventory items.
	 */
	NEW,

	/**
	 * Condition for good inventory items.
	 */
	GOOD,

	/**
	 * Condition for fair inventory items.
	 */
	FAIR,

	/**
	 * Condition for poor inventory items.
	 */
	POOR
}
