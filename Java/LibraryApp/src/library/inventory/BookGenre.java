package library.inventory;

/** 
 * Enum is a special "class" that represents a group of constants 
 * Only four valid Genre, which are FICTION, NONFICTION, BIOGRAPHY, and AUTOBIOGRAPHY.
 * 
 * @author Thomas Marxsen
 * @version 1.0
 * @since 2022.03.23
 */
public enum BookGenre {
	/**
	 * Genre for fiction books.
	 */
	FICTION,

	/**
	 * Genre for nonfiction books.
	 */
	NONFICTION,

	/**
	 * Genre for biography books.
	 */
	BIOGRAPHY,

	/**
	 * Genre for autobiography books.
	 */
	AUTOBIOGRAPHY
}
